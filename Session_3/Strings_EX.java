package Session_3;

public class Strings_EX {
public static void main(String[] args) {
	String s1 = "Thiru";//String literal
	String s2 = new String("Selvam");//String object
	char[] c1 = {'j','A','V','A'};//Array of characters 
	String s3 = new String(c1);//char to string
	System.out.println(s1);
	System.out.println(s2);
	System.out.println(s3);
}
}
