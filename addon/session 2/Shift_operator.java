package programs;

public class Shift_operator {
public static void main(String[] args) {
	//right shift
	System.out.println(20>>3);//20/3^2
	System.out.println(70>>4);//70/4^2
	
	//left shift --->operand * 2^num 
	System.out.println(40<<2);//40*2^2
	System.out.println(20<<3);//20*2^3j
	
}
}
